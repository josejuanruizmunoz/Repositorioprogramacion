public class Ejercicio2 {
    public static void main(String[] args) {
        //Crea un programa que defina una variable
        // llamada puntuación con valor inicial 0.
        // Luego, modifica su valor tres veces y
        // muestra el resultado final.
        //Ejemplo de salida por consola:
        //Puntuación inicial: 0
        //Después de primera modificación: 5
        //Después de segunda modificación: 10
        //Puntuación final: 15

        int puntuación = 0;
        System.out.println("Puntuación inicial: " + puntuación);

        puntuación = 5;
        System.out.println("Después de la primera modificación " + puntuación);

        puntuación = 10;
        System.out.println("Después de la segunda modificación " + puntuación);

        puntuación = 15;
        System.out.println("Después de la tercera modificación " + puntuación);
    }
}
